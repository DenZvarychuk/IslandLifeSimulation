package org.island.config.island;

public class BiomeConfig {
    private double scale;
    private double waterPercentage;
    private double fieldPercentage;

    public double getScale() {
        return scale;
    }

    public void setScale(double scale) {
        this.scale = scale;
    }

    public double getWaterPercentage() {
        return waterPercentage;
    }

    public void setWaterPercentage(double waterPercentage) {
        this.waterPercentage = waterPercentage;
    }

    public double getFieldPercentage() {
        return fieldPercentage;
    }

    public void setFieldPercentage(double fieldPercentage) {
        this.fieldPercentage = fieldPercentage;
    }
}
